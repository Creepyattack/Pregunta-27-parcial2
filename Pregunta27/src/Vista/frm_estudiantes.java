   public void selec_datos(){
        int fila = tbl_estudiante.getSelectedRow();
        this.lbl_id.setText(tbl_estudiante.getValueAt(fila, 0).toString());
        this.txt_carnet.setText(tbl_estudiante.getValueAt(fila, 1).toString());
        this.txt_nombres.setText(tbl_estudiante.getValueAt(fila, 2).toString());
        this.txt_apellidos.setText(tbl_estudiante.getValueAt(fila, 3).toString());
        this.txt_direccion.setText(tbl_estudiante.getValueAt(fila, 4).toString());
        this.txt_telefono.setText(tbl_estudiante.getValueAt(fila, 5).toString());
        this.txt_genero.setText(tbl_estudiante.getValueAt(fila, 6).toString());
        this.txt_email.setText(tbl_estudiante.getValueAt(fila, 7).toString());
        this.txt_fn.setText(tbl_estudiante.getValueAt(fila, 8).toString());
    }
    
