    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
        // TODO add your handling code here:
        obj_estudiante = new estudiante(0, this.txt_carnet.getText(),this.txt_nombres.getText(),this.txt_apellidos.getText(),this.txt_direccion.getText(),this.txt_telefono.getText(),this.txt_genero.getText(),this.txt_email.getText(),this.txt_fn.getText());
        obj_estudiante.agregar();
        tbl_estudiante.setModel(obj_estudiante.leer());
        this.txt_carnet.setText("");
        this.txt_nombres.setText("");
        this.txt_apellidos.setText("");
        this.txt_direccion.setText("");
        this.txt_telefono.setText("");
        this.txt_genero.setText("");
        this.txt_email.setText("");
        this.txt_fn.setText("");
        this.lbl_id.setText("0");
    }//GEN-LAST:event_btn_aceptarActionPerformed

